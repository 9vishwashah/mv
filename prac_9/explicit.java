public class explicit {
    public static void main(String[] args) {
        double d = 34.4643;
        long l = (long) d;
        System.out.println("After converting double Datatype to long:" + l);
        int n = (int) d;
        System.out.println("After converting double to int :" + n);
        int i = (int) l;
        System.out.println("After converting long to int Datatype: " + i);
    }
}
